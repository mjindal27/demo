
public class CalcMethods {

	
	
	public int add(int num1, int num2)
	{
		int i=num1;
		int j= num2;
		
		return i+j;
	}
	
	public int division(int num1, int num2)
	{
		int i=num1;
		int j= num2;
		
		return i/j;
	}
	
	public int multiply(int num1, int num2)
	{
		int i=num1;
		int j= num2;
		
		return i*j;
	}
	
	public int subtract(int num1, int num2)
	{
		int i=num1;
		int j= num2;
		
		return i-j;
	}
}
